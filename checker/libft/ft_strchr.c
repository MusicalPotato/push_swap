#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*st;

	st = (char *)s;
	while (*st)
	{
		if (*st == c)
			return (st);
		st++;
	}
	if (*st == c)
		return (st);
	return (0);
}
